# deklarasi variabel

nama = "Nabila"
umur = "23"

print(nama)
print(umur)

# aturan variabel
#a variabel harus mendefenisikan isi variabel tersebut
#b tidak boleh menggunakan spasi (snackcase pakai undercore (_) untuk variable 
    #atau pakai amelcase (menggunakan huruf besar (kebab case)) )
#c tidak boleh didahului dengan angka 
#d variabel tidak boleh sama (terganti)

# 5 tipe data
#1 string = sekumpulan karakter
nama_lengkap = "Tuti Nabila"
print(nama_lengkap)

#2 integer = angka
usia = 23
print(usia)

#3 float = bilangan berkoma
suhu = 30.3
print(suhu)

#4 bolean = false or true
cukup_umur = True
print(cukup_umur)

#5 Array
nama_buah = ["apel", "anggur", "jeruk", "mangga"]
biodata = ["nabila", "23"]
print(nama_buah)
